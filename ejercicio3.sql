CREATE TABLE COCHES(
    MATRICULA VARCHAR2(8) NOT NULL,
    MODELO VARCHAR2(25),
    MARCA VARCHAR2(25),
    FECHA_MATRICULACION DATE CONSTRAINT CK_FECHA CHECK (FECHA_MATRICULACION > TO_DATE ('1/1/2016','DD/MM/YYYY')),
    DNI VARCHAR2(9) NOT NULL,
        CONSTRAINT PK_COCHES PRIMARY KEY (MATRICULA),
        CONSTRAINT FK1_COC_PER FOREIGN KEY (DNI) REFERENCES PERSONAS(DNI)
