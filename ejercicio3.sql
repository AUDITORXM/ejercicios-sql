CREATE TABLE APARTAMENTOS(
    COD_APARTAMENTO NUMBER(6) NOT NULL,
    CALLE VARCHAR2(25) NOT NULL,
    NUMERO NUMBER(3) NOT NULL,
    PISO NUMBER(2),
    PUERTA VARCHAR2(8),
    SUPERFICIE NUMBER(8,2) CONSTRAINT CK_SUPERFICIE CHECK (SUPERFICIE>0),
    DNI VARCHAR2(9) NOT NULL,
        CONSTRAINT PK_APARTAMENTOS PRIMARY KEY (COD_APARTAMENTO),
        CONSTRAINT FK1_APA_PER FOREIGN KEY (DNI) REFERENCES PERSONAS(DNI)
)
